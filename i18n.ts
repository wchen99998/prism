const formatTemplate = (template: string, vars: Record<string, string | number> = {}) =>
  template.replace(/\{(\w+)\}/g, (_, key) => String(vars[key] ?? ''));

export const translate = (language: Language, key: UiKey, vars?: Record<string, string | number>) => {
  const template = uiStrings[language]?.[key] ?? uiStrings.en[key] ?? key;
  return formatTemplate(template, vars);
};

